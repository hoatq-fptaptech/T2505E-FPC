#include <stdio.h>
int main(){
	int x;
	x = 10;
	// NHẬP giá trị từ bàn phím vào x
	printf("Nhap x =");
	scanf("%d",&x);

	float pi = 3.141598;
	// NHẬP giá trị từ bàn phím vào pi
	printf("NHap pi=");
	scanf("%f",&pi);

	double px = 2.7628192;
	
	// NHẬP giá trị từ bàn phím vào px
	printf("NHap px=");
	scanf("%lf",&px);

	char ch = '@';
	// NHẬP giá trị từ bàn phím vào ch
	printf("NHap ch=");
	scanf("%c",&ch);
}